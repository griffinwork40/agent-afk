describe('checkGhReady — non-ENOENT version error (T1)', () => {
  it('returns ok: false with "failed unexpectedly" hint on EACCES', async () => {
    const exec = vi.fn().mockRejectedValue({ code: 'EACCES' });
    const result = await checkGhReady({ execFn: exec, ttlMs: 0 });
    expect(result.ok).toBe(false);
    if (!result.ok) {
      expect(result.hint).toMatch(/failed unexpectedly/);
    }
  });
});

// ---------------------------------------------------------------------------
// C5: In-flight dedup
// ---------------------------------------------------------------------------

describe('checkGhReady — in-flight dedup (C5)', () => {
  it('shares one probe when two calls arrive concurrently', async () => {
    const exec = vi.fn().mockResolvedValue({ stdout: '', stderr: '' });

    // Fire two calls simultaneously with no cache (ttlMs: 0)
    const [r1, r2] = await Promise.all([
      checkGhReady({ execFn: exec, ttlMs: 0 }),
      checkGhReady({ execFn: exec, ttlMs: 0 }),
    ]);

    // Both should succeed
    expect(r1.ok).toBe(true);
    expect(r2.ok).toBe(true);
    // Because of in-flight dedup, execFn should have been called only twice
    // (once for `gh --version`, once for `gh auth status`) — not 4 times.
    expect(exec).toHaveBeenCalledTimes(2);
  });
});

