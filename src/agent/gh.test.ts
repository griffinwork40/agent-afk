describe('createPr — timeout kind (P2)', () => {
  it('throws GhError with kind "timeout" when exec is killed', async () => {
    const exec = vi.fn().mockRejectedValue({ killed: true, code: null, stderr: '' });
    try {
      await createPr({ base: 'main', head: 'feat', title: 'T', body: 'B' }, exec);
      throw new Error('Expected createPr to throw');
    } catch (e: unknown) {
      expect(e).toBeInstanceOf(GhError);
      expect((e as GhError).kind).toBe('timeout');
    }
  });
});

// ---------------------------------------------------------------------------
// T1: Non-ENOENT error in checkGhReady
