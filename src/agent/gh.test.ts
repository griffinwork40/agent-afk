/**
 * Tests for src/agent/gh.ts
 *
 * All tests inject `execFn` — no real `gh` invocations occur.
 */

import { describe, it, expect, vi, afterEach } from 'vitest';

import {
  checkGhReady,
  createPr,
  postPrComment,
  resolveCurrentBranchPr,
  GhError,
  _resetCacheForTest,
} from './gh.js';

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/** Returns a mock execFn that resolves with the given output. */
function mockExec(stdout = '', stderr = '') {
  return vi.fn().mockResolvedValue({ stdout, stderr });
}

/** Returns a mock execFn that throws the given error. */
function mockExecThrows(err: unknown) {
  return vi.fn().mockRejectedValue(err);
}

// Each checkGhReady test must bypass the TTL cache; pass ttlMs: 0 everywhere.
const NO_CACHE = { ttlMs: 0 };

// File-scope reset: wipe module-level cache/inflight after every test so no
// test can leak state into the next one (regardless of which describe block
// they live in).
afterEach(() => {
  _resetCacheForTest();
});

// ---------------------------------------------------------------------------
// createPr
// ---------------------------------------------------------------------------

describe('createPr', () => {
  const baseOpts = {
    base: 'main',
    head: 'afk/farm/my-slug/1-branch-1',
    title: 'Auto PR: my-slug',
    body: 'Auto-generated by afk farm',
  };

  it('success — returns trimmed PR URL', async () => {
    const exec = mockExec('https://github.com/org/repo/pull/42\n', '');
    const result = await createPr(baseOpts, exec);
    expect(result).toBe('https://github.com/org/repo/pull/42');
    expect(exec).toHaveBeenCalledWith(
      'gh',
      expect.arrayContaining(['pr', 'create', '--base', 'main', '--head', 'afk/farm/my-slug/1-branch-1']),
    );
  });

  it('ENOENT — throws GhError with kind: not-found', async () => {
    const exec = mockExecThrows({ code: 'ENOENT' });
    await expect(createPr(baseOpts, exec)).rejects.toThrow(GhError);
    await expect(createPr(baseOpts, exec)).rejects.toMatchObject({ kind: 'not-found' });
  });

  it('unauthed — throws GhError with kind: unauthed', async () => {
    const exec = mockExecThrows({
      code: 'ENOENT',  // won't match since stderr is checked first — use non-ENOENT code
      stderr: 'authentication required: please log in to GitHub',
      exitCode: 1,
    });
    // Reset to non-ENOENT so stderr discrimination fires
    const exec2 = mockExecThrows({
      code: undefined,
      stderr: 'authentication required: please log in to GitHub',
      exitCode: 1,
    });
    await expect(createPr(baseOpts, exec2)).rejects.toMatchObject({ kind: 'unauthed' });
  });

  it('network error — throws GhError with kind: network', async () => {
    const exec = mockExecThrows({
      code: 'ETIMEDOUT',
      stderr: '',
      exitCode: 1,
    });
    await expect(createPr(baseOpts, exec)).rejects.toMatchObject({ kind: 'network' });
  });

  it('already-exists — throws GhError with kind: already-exists', async () => {
    const exec = mockExecThrows({
      code: undefined,
      stderr: 'a pull request for branch "afk/farm/my-slug/1-branch-1" already exists',
      exitCode: 1,
    });
    await expect(createPr(baseOpts, exec)).rejects.toMatchObject({ kind: 'already-exists' });
  });
});

// ---------------------------------------------------------------------------
// checkGhReady
// ---------------------------------------------------------------------------

describe('checkGhReady', () => {

  it('ENOENT on gh --version → ok: false with brew hint', async () => {
    const exec = mockExecThrows({ code: 'ENOENT' });
    const result = await checkGhReady({ execFn: exec, ...NO_CACHE });
    expect(result.ok).toBe(false);
    if (!result.ok) {
      expect(result.hint).toMatch(/brew install gh/);
    }
  });

  it('auth failure on gh auth status → ok: false with gh auth login hint', async () => {
    // First call succeeds (gh --version), second call fails (gh auth status)
    const exec = vi
      .fn()
      .mockResolvedValueOnce({ stdout: 'gh version 2.40.0', stderr: '' })
      .mockRejectedValueOnce(new Error('not logged in'));
    const result = await checkGhReady({ execFn: exec, ...NO_CACHE });
    expect(result.ok).toBe(false);
    if (!result.ok) {
      expect(result.hint).toMatch(/gh auth login/);
    }
  });

  it('both succeed → ok: true', async () => {
    const exec = vi
      .fn()
      .mockResolvedValueOnce({ stdout: 'gh version 2.40.0', stderr: '' })
      .mockResolvedValueOnce({ stdout: 'Logged in to github.com', stderr: '' });
    const result = await checkGhReady({ execFn: exec, ...NO_CACHE });
    expect(result).toEqual({ ok: true });
  });

  it('TTL cache: second call within TTL returns cached result without calling execFn again', async () => {
    const exec = vi
      .fn()
      .mockResolvedValueOnce({ stdout: 'gh version 2.40.0', stderr: '' })
      .mockResolvedValueOnce({ stdout: 'Logged in', stderr: '' });

    const now = Date.now();
    const _now = vi.fn().mockReturnValue(now);

    // First call: execFn called twice (version + auth)
    const first = await checkGhReady({ execFn: exec, ttlMs: 60_000, _now });
    expect(exec).toHaveBeenCalledTimes(2);
    expect(first).toEqual({ ok: true });

    // Second call at same timestamp: cache hit — execFn NOT called again
    const second = await checkGhReady({ execFn: exec, ttlMs: 60_000, _now });
    expect(exec).toHaveBeenCalledTimes(2); // still 2
    expect(second).toEqual({ ok: true });
  });
});

