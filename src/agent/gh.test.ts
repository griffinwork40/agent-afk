describe('classifyStderr — broadened unauth patterns (C1)', () => {
  const patterns = [
    'HTTP 401 unauthorized',
    'HTTP 403 forbidden',
    'bad credentials',
    'invalid token',
    'insufficient scope',
  ];

  for (const stderr of patterns) {
    it(`classifies "${stderr}" as unauthed`, async () => {
      const exec = vi.fn().mockRejectedValue({ stderr, code: undefined, killed: false });
      try {
        await createPr({ base: 'main', head: 'feat', title: 'T', body: 'B' }, exec);
        throw new Error('Expected createPr to throw');
      } catch (e: unknown) {
        expect(e).toBeInstanceOf(GhError);
        expect((e as GhError).kind).toBe('unauthed');
      }
    });
  }
});

// ---------------------------------------------------------------------------
// P1: Failure results are NOT cached
// ---------------------------------------------------------------------------

describe('checkGhReady — failure not cached (P1)', () => {
  it('re-probes after a failed check even within the TTL window', async () => {
    let callCount = 0;
    const exec = vi.fn().mockImplementation(async () => {
      callCount++;
      if (callCount <= 1) throw new Error('first call fails');
      return { stdout: '', stderr: '' };
    });

    const fixedNow = Date.now();
    const opts = { execFn: exec, _now: () => fixedNow };

    const first = await checkGhReady(opts);
    expect(first.ok).toBe(false);

    // Second call — failure was NOT cached, so execFn must be called again
    const second = await checkGhReady(opts);
    expect(exec).toHaveBeenCalledTimes(3); // 1 for first (threw) + 2 for second (version + auth)
    expect(second.ok).toBe(true);
  });
});

